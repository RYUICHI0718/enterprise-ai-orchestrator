def main():
    entries = []
    lines = RAW_DATA.strip().split('\n')
    
    for i, line in enumerate(lines, 1):
        if not line.strip():
            continue
            
        parts = line.split('\t')
        
        # Ensure we have at least 3 parts (Category, Question, Answer)
        if len(parts) >= 3:
            category = parts[0].strip()
            question = parts[1].strip()
            answer = parts[2].strip()
            
            # Use 'JHF-XXX' format for ID
            item_id = f"JHF-{i:03d}"
            
            entries.append({
                "id": item_id,
                "category": category,
                "question": question,
                "answer": answer
            })
    
    # Target Path
    backend_dir = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
    target_path = os.path.join(backend_dir, "data", "faq.json")
    
    with open(target_path, "w", encoding="utf-8") as f:
        json.dump(entries, f, indent=4, ensure_ascii=False)
        
    print(f"Successfully processed {len(entries)} items.")
    print(f"Saved to: {target_path}")

if __name__ == "__main__":
    main()
